// INA219 sensor connects with I2C
// INA219 pin = Arduino pin
//
// if using Arduino MKR Zero
// SDA is arduino pin 11
// SCL is arduino pin 12
//
// if using Teensy 4.1
// SDA is pin 18
// SCL is pin 19
//
// if using QWIIC connector:
// red = 3.3V
// black = gnd
// blue = SDA 
// yellow = SCL 
//
// otherwise connect each pin manually